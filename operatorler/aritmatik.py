# 4 islem operatörleri 
a = 10
b = 5
c = a + b  # toplama
d = a - b  # çıkarma
e = a * b  # çarpma
f = a / b  # bölme
g = a // b # tam bölme (kalansiz bölme)
h = a % b  # mod alma 
print(c,d,e,f,g,h)


# mod alma operatörünün kullanımına bir örnek verelim. Mod alma operatörü, 
# bir sayının diğer sayıya bölümünden kalanını verir.
#  Örneğin, 10 mod 3 işlemi 1 sonucunu verir çünkü 10'u 3'e böldüğümüzde kalan 1'dir.

mod_sonuc = 10 % 3
print(mod_sonuc)  # Çıktı: 1
